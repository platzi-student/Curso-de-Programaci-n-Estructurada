
float sumar(float valorA, float valorB)
{
    return valorA + valorB;
}

float restar(float valorA, float valorB)
{
    return valorA - valorB;
}

float multiplicar(float valorA, float valorB)
{
    return valorA * valorB;
}

float dividir(float valorA, float valorB)
{
    return valorA / valorB;
}
